// Fills src/data/divisionHindiPhrasesGenerated.js by translating the English
// text runs of every division body (CMS rsac_pages, section=divisions) to Hindi.
// OfficialContentPage renders the English body (exact layout) and swaps each run
// to Hindi via this map, so Hindi division pages match English with Hindi text.
//
// Translation engine: keyless Google endpoint by default (no credentials). Set
// BHASHINI_USER_ID + BHASHINI_ULCA_API_KEY in .env.local to use Bhashini instead.
//
// Re-runnable: existing translations are reused; only new runs are sent.
// Usage: node scripts/build-division-hindi-phrases.mjs

import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const directusUrl = "http://localhost:8055";
const repoRoot = resolve(import.meta.dirname, "..");
const outputPath = resolve(repoRoot, "src/data/divisionHindiPhrasesGenerated.js");

const loadEnv = async () => {
  for (const file of [".env.local", ".env"]) {
    try {
      const raw = await readFile(resolve(repoRoot, file), "utf8");
      for (const line of raw.split(/\r?\n/)) {
        const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/);
        if (m && !process.env[m[1]]) process.env[m[1]] = m[2].replace(/^["']|["']$/g, "");
      }
    } catch {
      /* optional */
    }
  }
};

const decode = (v) =>
  v
    .replace(/&nbsp;/gi, " ")
    .replace(/&amp;/gi, "&")
    .replace(/&#39;/gi, "'")
    .replace(/&lsquo;|&rsquo;/g, "'")
    .replace(/&ldquo;|&rdquo;/g, '"')
    .replace(/&ndash;|&mdash;/g, "-")
    .replace(/\s+/g, " ")
    .trim();

// Extract translatable runs from a body: visible text with Latin letters, not
// pure numbers / emails / urls / dates.
const extractRuns = (html) => {
  const runs = new Set();
  for (const piece of html.replace(/<[^>]+>/g, "\x01").split("\x01")) {
    const t = decode(piece);
    if (
      t.length >= 3 &&
      /[A-Za-z]/.test(t) &&
      !/^[\w.+-]+@[\w.-]+$/.test(t) &&
      !/^https?:\/\//.test(t)
    ) {
      runs.add(t);
    }
  }
  return runs;
};

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

// Keyless Google translate endpoint. Returns Hindi for one string.
const gtxTranslate = async (text) => {
  const url =
    "https://translate.googleapis.com/translate_a/single?client=gtx&sl=en&tl=hi&dt=t&q=" +
    encodeURIComponent(text);
  for (let attempt = 0; attempt < 4; attempt++) {
    try {
      const res = await fetch(url, { headers: { "User-Agent": "Mozilla/5.0" } });
      if (res.status === 429) {
        await sleep(1500 * (attempt + 1));
        continue;
      }
      if (!res.ok) throw new Error(`${res.status}`);
      const data = await res.json();
      const hi = (data[0] || []).map((seg) => seg[0]).join("");
      if (hi && /[ऀ-ॿ]/.test(hi)) return hi;
      return "";
    } catch {
      await sleep(800 * (attempt + 1));
    }
  }
  return "";
};

const loadExisting = async () => {
  try {
    const mod = await import(`file://${outputPath}?t=${Date.now()}`);
    return { ...mod.divisionHindiPhrasesGenerated };
  } catch {
    return {};
  }
};

const run = async () => {
  await loadEnv();

  const res = await fetch(
    `${directusUrl}/items/rsac_pages?fields=slug,html,title,summary&filter[section_key][_eq]=divisions&limit=-1`
  );
  const pages = (await res.json()).data;

  const allRuns = new Set();
  for (const page of pages) {
    if (page.html) for (const r of extractRuns(page.html)) allRuns.add(r);
    for (const field of [page.title, page.summary]) {
      const t = field && decode(field);
      if (t && t.length >= 3 && /[A-Za-z]/.test(t)) allRuns.add(t);
    }
  }

  const map = await loadExisting();
  const todo = [...allRuns].filter((r) => !map[r]);
  process.stdout.write(
    `runs total=${allRuns.size} cached=${allRuns.size - todo.length} toTranslate=${todo.length}\n`
  );

  const CONCURRENCY = 10;
  let idx = 0;
  let done = 0;
  const worker = async () => {
    while (idx < todo.length) {
      const en = todo[idx++];
      const hi = await gtxTranslate(en);
      if (hi) map[en] = hi;
      done++;
      if (done % 50 === 0 || done === todo.length) {
        process.stdout.write(`  ${done}/${todo.length}\n`);
        await flush(map); // checkpoint so progress survives interruption
      }
    }
  };
  await Promise.all(Array.from({ length: CONCURRENCY }, worker));

  await flush(map);
  process.stdout.write(`\nWrote ${outputPath} (${Object.keys(map).length} phrases)\n`);
};

const flush = async (map) => {
  const sorted = Object.fromEntries(Object.entries(map).sort(([a], [b]) => a.localeCompare(b)));
  await writeFile(
    outputPath,
    `// Generated by scripts/build-division-hindi-phrases.mjs. Do not edit by hand.\n// Hand-authored entries in divisionHindiPhrases.js override these.\n\nexport const divisionHindiPhrasesGenerated = ${JSON.stringify(sorted, null, 2)};\n`,
    "utf8"
  );
};

run().catch((e) => {
  process.stderr.write(`${e.stack || e.message}\n`);
  process.exit(1);
});
